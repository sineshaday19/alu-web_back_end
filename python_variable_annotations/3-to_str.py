#!/usr/bin/env python3


"""
This module contains a function that
converts a float to a string.

Example:
    >>> to_str(3.14)
    '3.14'
"""


def to_str(n: float) -> str:
    """
    Convert a float to a string.

    Args:
        n (float): The float to convert.

    Returns:
        str: The string representation of the float.

    Example:
        >>> to_str(3.14)
        '3.14'
    """
    return str(n)
